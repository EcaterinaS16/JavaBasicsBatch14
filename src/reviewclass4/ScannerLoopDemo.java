package reviewclass4;

public class ScannerLoopDemo {

	public static void main(String[] args) {
		//nested for loop
	
      
		for(int j=0;j<3;j++) {
			for(int i=0;i<3;i++) {
			System.out.println(i);
			}
			System.out.println("         ");
		// while nested
			
			for(int k=0;k<5;k++) {
				int i=0;
				while(i<3) {
					System.out.println(i);
					i++;
				}
			}
}}}